export type SamplerType = {
  sampler: {
    urls: {
      [note: string]: string;
    };
    release: number;
    baseUrl: string;
  };
  label:
    | FgPianosLabels
    | FgGuitarsLabels
    | FgStringsLabels
    | FgWindsLabels
    | FgDrumsLabels
    | FgSynthsLabels
    | FgOrgansLabels
    | FgMiscLabels;
  playOnlyDefined: boolean;
};

